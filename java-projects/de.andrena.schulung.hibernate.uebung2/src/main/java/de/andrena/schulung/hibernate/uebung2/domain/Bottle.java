package de.andrena.schulung.hibernate.uebung2.domain;

public class Bottle {

	private BottleSize bottleSize;
	private Beer beer;

	private Long id;

	public BottleSize getBottleSize() {
		return bottleSize;
	}

	public void setBottleSize(BottleSize bottleSize) {
		this.bottleSize = bottleSize;
	}

	public Beer getBeer() {
		return beer;
	}

	public void setBeer(Beer beer) {
		this.beer = beer;
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

}
